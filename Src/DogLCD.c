void sendTextAt(uint8_t Data[], uint8_t posX, uint8_t posY) {
	setCursorPosition(posX, posY);
	while (*Data != '\0')
		sendFont6x8(*Data++);
}

void sendDataAt3(const uint8_t Data[], uint8_t posX, uint8_t posY, uint8_t columns, uint8_t rows) {

	uint8_t row, column;

	for (row = 0; row < rows; row++) {
		setCursorPosition(posX, posY + row);
		for (column = 0; column < columns; column++) {
			HAL_GPIO_WritePin(GPIOC, LCD_CS_Pin, GPIO_PIN_RESET); //chip select on
			HAL_GPIO_WritePin(GPIOC, LCD_CD_Pin, GPIO_PIN_SET); //data
			HAL_SPI_Transmit(&hspi2, Data, 1, 1000);
			HAL_GPIO_WritePin(GPIOC, LCD_CS_Pin, GPIO_PIN_SET); //chip select off
			Data++;
		}
	}
}

uint8_t sendTextAt2(uint8_t Data[], uint8_t len, uint8_t posX, uint8_t posY) {

	int16_t i;

	setCursorPosition(posX, posY);
	for (i = len + (40 - len % 40); i >= 0; i--)
		sendFont6x8(' ');

	setCursorPosition(posX, posY);
	while (*Data != '\0')
		sendFont6x8(*Data++);

	return (len + (40 - len % 40)) / 40;
}

void sendDataAt(uint8_t Data[], uint8_t posX, uint8_t posY, uint8_t columns, uint8_t rows) {

	uint8_t row, column;

	for (row = 0; row < rows; row++) {
		setCursorPosition(posX, posY + row);
		for (column = 0; column < columns; column++)
			sendFont6x8(*Data++);
	}
}

void sendFont6x8(uint8_t inChar) {
	HAL_GPIO_WritePin(GPIOC, LCD_CS_Pin, GPIO_PIN_RESET); 	//chip select on
	HAL_GPIO_WritePin(GPIOC, LCD_CD_Pin, GPIO_PIN_SET);		//data
	HAL_SPI_Transmit(&hspi2, &font_6x8[inChar][0], 6, 5000);
	HAL_GPIO_WritePin(GPIOC, LCD_CS_Pin, GPIO_PIN_SET); 	//chip select off
}

void sendCommand(uint8_t Cmd) {

	HAL_GPIO_WritePin(GPIOC, LCD_CS_Pin, GPIO_PIN_RESET); 	//chip select on
	HAL_GPIO_WritePin(GPIOC, LCD_CD_Pin, GPIO_PIN_RESET);	//cmd
	HAL_SPI_Transmit(&hspi2, &Cmd, 1, 5000);				//send data
	HAL_GPIO_WritePin(GPIOC, LCD_CS_Pin, GPIO_PIN_SET);		//chip select off
}

void setCursorPosition(uint8_t posX, uint8_t posY) {
	sendCommand((posX >> 4) | 0b00010000); 					//MSB adress column
	sendCommand(posX & 0x0F); 								//LSB adress column

	sendCommand((posY & 0x0F) | 0b01100000); 				//LSB page
	sendCommand(((posY >> 4) & 0x0F) | 0b01110000); 		//MSB
}

