void extractOneTenHundredThousands(int value, uint8_t *ones, uint8_t *tens,
		uint8_t *hundreds, uint8_t *thousands, uint8_t *sign) {
	*thousands = 0;
	*hundreds = 0;
	*tens = 0;

	if (value < 0) {
		*sign = 0; //minus
		value *= -1; //vnder p talet
	} else
		*sign = 1; //plus

	while (value >= 1000) {
		value -= 1000;
		++*thousands;
	}
	while (value >= 100) {
		value -= 100;
		++*hundreds;
	}
	while (value >= 10) {
		value -= 10;
		++*tens;
	}
	*ones = value;
}

